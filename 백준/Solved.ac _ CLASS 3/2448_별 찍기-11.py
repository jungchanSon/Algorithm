import sys
input = sys.stdin.readline
N = int(input())
M = N*2

star_board = [[" "] * M for _ in range(N)]
def draw_start(x, y):
    star_board[y][x-1] = "*"
    star_board[y+1][x-2] = "*"
    star_board[y+1][x] = "*"
    
    for i in range(5):
        star_board[y+2][x-3+i] = "*"
        
def recursion(x, y, h,  space_length):

    if h <= 3:
        draw_start(x, y)
        return
    recursion(x, y, h//2, 0)
    recursion(x - h//2, y + h//2, h//2, 0)
    recursion(x + h//2, y + h//2, h//2, 0)
recursion(M//2, 0, N ,N//2)
